#!/usr/bin/env python3
"""
小单占比与主力吸筹/派发关系函数推导
基于2026年4月3日实盘数据
"""

import numpy as np

print("=" * 60)
print("  小单占比与主力吸筹/派发关系函数推导")
print("=" * 60)

# ============================================================
# 今日实盘数据
# ============================================================

print("\n" + "=" * 60)
print("  一、今日实盘数据")
print("=" * 60)

# 002788 鹭燕 09:26
print("\n【002788 鹭燕 09:26】")
buy_0926 = {'vol': 142, 'orders': 14, 'small_vol': 142}
sell_0926 = {'vol': 46, 'orders': 2, 'small_vol': 46}

S_b_0926 = buy_0926['small_vol'] / buy_0926['vol']
S_s_0926 = sell_0926['small_vol'] / sell_0926['vol']
print(f"  买盘小单占比 S_b = {S_b_0926*100:.1f}%")
print(f"  卖盘小单占比 S_s = {S_s_0926*100:.1f}%")

# 002788 鹭燕 09:37
print("\n【002788 鹭燕 09:37】")
buy_0937 = {'vol': 2600, 'orders': 275, 'small_vol': 2210, 'large_vol': 390}
sell_0937 = {'vol': 46, 'orders': 2, 'small_vol': 46}

S_b_0937 = buy_0937['small_vol'] / buy_0937['vol']
S_s_0937 = 1.0  # 卖盘全是小单
print(f"  买盘小单占比 S_b = {S_b_0937*100:.1f}%")
print(f"  卖盘小单占比 S_s = {S_s_0937*100:.1f}%")

# 600666 奥瑞德 09:27
print("\n【600666 奥瑞德 09:27】")
buy_666 = {'vol': 13300, 'orders': 2, 'small_vol': 0}
sell_666 = {'vol': 560, 'orders': 15, 'small_vol': 370}

S_b_666 = buy_666['small_vol'] / buy_666['vol']
S_s_666 = sell_666['small_vol'] / sell_666['vol']
print(f"  买盘小单占比 S_b = {S_b_666*100:.1f}%")
print(f"  卖盘小单占比 S_s = {S_s_666*100:.1f}%")

# ============================================================
# 函数推导
# ============================================================

print("\n" + "=" * 60)
print("  二、函数1：小单占比差值函数")
print("=" * 60)

print("""
定义：ΔS = S_s - S_b

物理意义：
  ΔS > 0 → 卖盘更多是小单 → 散户在卖，主力在买 → 吸筹
  ΔS < 0 → 买盘更多是小单 → 散户在买，主力在卖 → 派发
""")

delta_0926 = S_s_0926 - S_b_0926
delta_0937 = S_s_0937 - S_b_0937
delta_666 = S_s_666 - S_b_666

print(f"  鹭燕 09:26: ΔS = {S_s_0926:.2f} - {S_b_0926:.2f} = {delta_0926:.2f} → {'吸筹' if delta_0926 > 0 else '派发'}")
print(f"  鹭燕 09:37: ΔS = {S_s_0937:.2f} - {S_b_0937:.2f} = {delta_0937:.2f} → {'吸筹' if delta_0937 > 0 else '派发'}")
print(f"  奥瑞德 09:27: ΔS = {S_s_666:.2f} - {S_b_666:.2f} = {delta_666:.2f} → {'吸筹' if delta_666 > 0 else '派发'}")

print("\n" + "=" * 60)
print("  三、函数2：主力行为强度函数")
print("=" * 60)

print("""
引入买卖比 R = V_b / V_s

定义主力行为强度：
  F = ΔS × ln(R)

物理意义：
  F > 0 → 吸筹（卖盘小单多 + 买盘量大）
  F < 0 → 派发（买盘小单多 + 卖盘量大）
  |F| 越大，主力行为越明显
""")

R_0926 = buy_0926['vol'] / sell_0926['vol']
R_0937 = buy_0937['vol'] / sell_0937['vol']
R_666 = buy_666['vol'] / sell_666['vol']

F_0926 = delta_0926 * np.log(R_0926)
F_0937 = delta_0937 * np.log(R_0937)
F_666 = delta_666 * np.log(R_666)

print(f"  鹭燕 09:26: F = {delta_0926:.2f} × ln({R_0926:.1f}) = {delta_0926:.2f} × {np.log(R_0926):.2f} = {F_0926:.2f}")
print(f"  鹭燕 09:37: F = {delta_0937:.2f} × ln({R_0937:.1f}) = {delta_0937:.2f} × {np.log(R_0937):.2f} = {F_0937:.2f}")
print(f"  奥瑞德 09:27: F = {delta_666:.2f} × ln({R_666:.1f}) = {delta_666:.2f} × {np.log(R_666):.2f} = {F_666:.2f}")

print("\n" + "=" * 60)
print("  四、函数3：吸筹概率函数（Sigmoid）")
print("=" * 60)

print("""
将F映射到[0,1]区间：

  P(吸筹) = 1 / (1 + e^(-kF))

经验值 k = 0.5

判断标准：
  P > 0.7 → 高概率吸筹
  P < 0.3 → 高概率派发
  0.3 ≤ P ≤ 0.7 → 多空平衡
""")

k = 0.5
P_0926 = 1 / (1 + np.exp(-k * F_0926))
P_0937 = 1 / (1 + np.exp(-k * F_0937))
P_666 = 1 / (1 + np.exp(-k * F_666))

print(f"  鹭燕 09:26: P = {P_0926:.3f} = {P_0926*100:.1f}%")
print(f"  鹭燕 09:37: P = {P_0937:.3f} = {P_0937*100:.1f}%")
print(f"  奥瑞德 09:27: P = {P_666:.3f} = {P_666*100:.1f}%")

print("\n" + "=" * 60)
print("  五、函数4：量能修正因子")
print("=" * 60)

print("""
缩量是验证器：

  Q = V_today / V_avg5

修正规则：
  下跌时：F' = F / Q（缩量下跌放大吸筹信号）
  上涨时：F' = F × Q（缩量上涨确认控盘）
""")

# 600666 量能数据
V_today_331 = 637  # 3/31 成交量(万手)
V_today_401 = 264  # 4/1 成交量
V_avg5 = 643       # 近5日均量

Q_331 = V_today_331 / V_avg5
Q_401 = V_today_401 / V_avg5

print(f"  600666 3/31 大跌: Q = {V_today_331}/{V_avg5} = {Q_331:.2f}")
print(f"    修正: F' = F / {Q_331:.2f} = F × {1/Q_331:.2f}（放大信号）")
print(f"  600666 4/1 涨停: Q = {V_today_401}/{V_avg5} = {Q_401:.2f}")
print(f"    修正: F' = F × {Q_401:.2f}（缩小确认控盘）")

print("\n" + "=" * 60)
print("  六、函数5：综合判断函数")
print("=" * 60)

print("""
最终信号：

  Signal = sign(F') × P(吸筹) × Q

判断标准：
  Signal > 0.5  → 强烈吸筹，买入
  Signal > 0    → 弱吸筹，持有
  Signal < -0.5 → 强烈派发，卖出
  Signal < 0    → 弱派发，减仓
  Signal ≈ 0    → 观望
""")

# 综合计算
print("\n【今日数据综合判断】")

# 鹭燕 09:26
S_0926 = np.sign(F_0926) * P_0926  # 无量能修正，Q=1
print(f"\n  鹭燕 09:26:")
print(f"    Signal = sign({F_0926:.2f}) × {P_0926:.3f} × 1.0 = {S_0926:.3f}")
print(f"    判断: {'强烈吸筹' if S_0926 > 0.5 else '吸筹' if S_0926 > 0 else '派发'}")

# 鹭燕 09:37
S_0937 = np.sign(F_0937) * P_0937
print(f"\n  鹭燕 09:37:")
print(f"    Signal = sign({F_0937:.2f}) × {P_0937:.3f} × 1.0 = {S_0937:.3f}")
print(f"    判断: {'强烈吸筹' if S_0937 > 0.5 else '吸筹' if S_0937 > 0 else '派发'}")

# 奥瑞德 09:27
S_666 = np.sign(F_666) * P_666
print(f"\n  奥瑞德 09:27:")
print(f"    Signal = sign({F_666:.2f}) × {P_666:.3f} × 1.0 = {S_666:.3f}")
print(f"    判断: {'强烈吸筹' if S_666 > 0.5 else '吸筹' if S_666 > 0 else '派发'}")

